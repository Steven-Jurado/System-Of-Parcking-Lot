    /**
     * <p> Este metodo esta creado para la facturacion 
     * lo que hace es que espera que el elemento que manda a buscar el boton  que lo encuentre 
     * y luego lo elimine para que no se genere dos factura con el mismo ruco cedula </p>
     */
    void eliminar(){
        
        try {
            String SQL = "DELETE FROM factura WHERE ruc =\'"+jtxtBuscar.getText().trim()+"\'";
            PreparedStatement preparar =con.prepareStatement(SQL);
            preparar.executeUpdate();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex, "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    public void factura(String aux){
        try {
            Map <String , String> parametro = new HashMap<String, String>();
            parametro.put("cedula", aux);
            JasperReport Reporte = (JasperReport) JRLoader.loadObject(getClass().getResource("/reportes/Factura.jasper"));
            JasperPrint Imprimir = JasperFillManager.fillReport(Reporte,parametro, con);
            JasperViewer ver = new JasperViewer(Imprimir, false);
            ver.setTitle("Factura");
            ver.setIconImage(new ImageIcon(getClass().getResource("/Icon/Carro.png")).getImage());
            ver.setVisible(true);
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    

