    private void jtxtBuscarvehiculoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtBuscarvehiculoKeyTyped
        if(jtxtBuscarvehiculo.getText().length() > (8 -1)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limite", "Error",JOptionPane.ERROR_MESSAGE);
        }
        char caracteres = evt.getKeyChar();
        if((caracteres < 65 || caracteres > 90) && (caracteres < 97 || caracteres >122 ) && (caracteres <48 || caracteres >57) 
                && (caracteres != 8) && (caracteres != 10 ) ){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Caracter Denegado ", "Error",JOptionPane.ERROR_MESSAGE);
        }else   
            Buscar();
    }//GEN-LAST:event_jtxtBuscarvehiculoKeyTyped

