    public void search(){
        try {
            Connection con = ConectarBD.ConexcionBD.Conectar();
            String Sql = "SELECT * FROM Vehiculo WHERE placa like \'%"+jtxtBuscarvehiculo.getText().trim()+"%\'";
            PreparedStatement preparar = con.prepareStatement(Sql);
            ResultSet Resultado = preparar.executeQuery();
            DefaultTableModel Tabla = (DefaultTableModel) jTable1.getModel();
            Tabla.getDataVector().clear();
            while (Resultado.next()) {
                Object[] Datos = new Object[7];
                Datos[0] = Resultado.getString("id_vehiculo");
                Datos[1] = Resultado.getString("placa");
                Datos[2] = Resultado.getString("modelo");
                Datos[3] = Resultado.getString("propietario");
                Datos[4] = Resultado.getString("tipo_vehiculo");
                Datos[6] = Resultado.getString("hora_entrada");
                Datos[5] = Resultado.getString("descripcion");
                Tabla.addRow(Datos);
            }
            jTable1.setModel(Tabla);
        } catch (ClassNotFoundException | SQLException e) {
        }
        
    }

