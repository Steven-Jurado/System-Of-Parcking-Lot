    @Override
    public void Insertar(Carro car, Moto mot) {
        try {
            Connection con = ConectarBD.ConexcionBD.Conectar();
            String SQL = "INSERT INTO  Vehiculo (placa,modelo,propietario,tipo_vehiculo,hora_entrada,descripcion,fecha_entrada,marca) VALUES (?,?,?,?,?,?,?,?)";
            PreparedStatement preparar = con.prepareStatement(SQL);
            if (jcombotipovehiculo.getSelectedIndex() == 0) {
                preparar.setString(1, car.getPlaca());
                preparar.setString(2, car.getModelo());
                preparar.setString(3, car.getPropietarioVehiculo());
                preparar.setString(4, jcombotipovehiculo.getSelectedItem().toString().toUpperCase());
                preparar.setString(5, car.getHora());
                preparar.setString(6, car.getDrescripcion());
                preparar.setString(7, car.getFechaEntrada());
                preparar.setString(8, car.getMarca());
                preparar.executeUpdate();
            }else{
                preparar.setString(1, mot.getPlaca());
                preparar.setString(2, mot.getModelo());
                preparar.setString(3, mot.getPropietarioVehiculo());
                preparar.setString(4, jcombotipovehiculo.getSelectedItem().toString().toUpperCase());
                preparar.setString(5, mot.getHora());
                preparar.setString(6, mot.getDrescripcion());
                preparar.setString(7, mot.getFechaEntrada());
                preparar.setString(8, mot.getMarca());
                preparar.executeUpdate();
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex , "Error",JOptionPane.ERROR_MESSAGE);
        }
    }

