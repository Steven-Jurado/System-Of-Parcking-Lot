    private void BotonAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonAgregarMouseClicked
        if (jtctplaca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Placa Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtpropietario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Propietario Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtmarca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Marca Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtModelo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Modelo Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtareadescripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Descripcion Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (jcombotipovehiculo.getSelectedIndex() == 0) {
                carro = new Carro(jtctplaca.getText().trim(),
                        jtxtpropietario.getText().trim(), jtxtmarca.getText().trim());
                carro.setDrescripcion(jtxtareadescripcion.getText().toUpperCase());
                carro.setModelo(jtxtModelo.getText().trim());
                JOptionPane.showMessageDialog(null, "Datos Ingresados: " + carro.getPlaca().trim(), "Agregado ", JOptionPane.INFORMATION_MESSAGE);
                Limpiar();
                Insertar(carro, null);

            } else {
                Moto moto = new Moto(jtctplaca.getText().trim(), jtxtpropietario.getText().trim(), jtxtmarca.getText().trim());
                moto.setDrescripcion(jtxtareadescripcion.getText().toUpperCase());
                JOptionPane.showMessageDialog(null, "Datos Ingresados: " + moto.getPlaca().trim(), "Agregado ", JOptionPane.INFORMATION_MESSAGE);
                moto.setModelo(jtxtModelo.getText().trim());
                moto.setDrescripcion(jtxtareadescripcion.getText().toUpperCase());
                Limpiar();
                Insertar(null, moto);
            }
        }
        
    }//GEN-LAST:event_BotonAgregarMouseClicked

