    public void Insertar(Empleado empleado)  {
        
        try {
            Connection con = ConectarBD.ConexcionBD.Conectar();
            String SQL = "INSERT INTO persona (cedula,nombre,apellido,telefono,tipo_persona,fecha_entrada,correo) VALUES (?,?,?,?,?,?,?)";

            if (jComboBox1.getSelectedIndex() == 0) {
                agregar(SQL, con, empleado);

            } else {
                agregar(SQL, con, empleado);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex , "Error",JOptionPane.ERROR_MESSAGE);
        }
        
        
    }
    
    public void agregar(String SQL , Connection con , Empleado empleado) {
        
        try {
            PreparedStatement preparar = con.prepareStatement(SQL);
            preparar.setString(1, empleado.getCedula().toUpperCase().trim());
            preparar.setString(2, empleado.getNombre().toUpperCase());
            preparar.setString(3, empleado.getApellido().toUpperCase());
            preparar.setString(4, empleado.getTelefono().toUpperCase());
            preparar.setString(5, jComboBox1.getSelectedItem().toString().toUpperCase());
            preparar.setString(6, jlabelFecha.getText().trim().toUpperCase());
            preparar.setString(7, jtxtCorreo.getText().toUpperCase());
            preparar.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos Guardados" , "Guardado" ,JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex , "Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
