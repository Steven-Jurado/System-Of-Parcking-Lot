    @Override
    public void listar() {
        try {
            Connection con = ConectarBD.ConexcionBD.Conectar();
            String SQL = "SELECT * FROM persona";
            PreparedStatement preparar = con.prepareStatement(SQL);
            ResultSet Resultado = preparar.executeQuery();
            DefaultTableModel Tabla = (DefaultTableModel) jTable1.getModel();
            Tabla.getDataVector().clear();
            jTable1.updateUI();
            while (Resultado.next()) {
                Object[] Datos = new Object[7];
                Datos[0] = Resultado.getString("cedula");
                Datos[1] = Resultado.getString("nombre");
                Datos[2] = Resultado.getString("apellido");
                Datos[3] = Resultado.getString("fecha_entrada");
                Datos[4] = Resultado.getString("tipo_persona");
                Datos[5] = Resultado.getString("telefono");
                Datos[6] = Resultado.getString("correo");
                Tabla.addRow(Datos);
            }
            jTable1.setModel(Tabla);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex, "Error", JOptionPane.ERROR_MESSAGE);

        }
    }

