    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (jtxtCedula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Cedula Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Nombre Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtApellido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Apellido Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtCorreo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo correo Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtTelefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo telefono Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtSueldo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo Sueldo Vacio", "ERROR", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Double aux = Double.parseDouble(jtxtSueldo.getText().trim());
            Empleado empleado = new Empleado(aux, jtxtCedula.getText().trim(), jtxtNombre.getText().toUpperCase(),
                    jtxtApellido.getText().toUpperCase());
            empleado.setTelefono(jtxtTelefono.getText().trim());
            Insertar(empleado);
            Limpiar();
        }
    }//GEN-LAST:event_jButton1MouseClicked

