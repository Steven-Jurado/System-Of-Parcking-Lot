    private void botonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEntrarActionPerformed
        if (jtxtusuarip.getText().equals("Admin") && jpasscontrasenia.getText().equals("1234")) {
            Ejecucion.Inicio.MenuMantenimiento.setEnabled(true);
            Ejecucion.Inicio.JMenuReporte.setEnabled(true);
            Ejecucion.Inicio.MenuLogin.setText("ADMINISTRADOR");
            iniciar.setVisible(true);
            this.setVisible(false);
        }else if (jtxtusuarip.getText().equals("Empleado") && jpasscontrasenia.getText().equals("1234")){
            Ejecucion.Inicio.MenuMantenimiento.setEnabled(false);
            Ejecucion.Inicio.JMenuReporte.setEnabled(false);
            Ejecucion.Inicio.MenuLogin.setText("EMPLEADO");
            iniciar.setVisible(true);
            this.setVisible(false);
        }
        else
        {
            jtxtusuarip.requestFocus();
            JOptionPane.showMessageDialog(null, "Error ", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonEntrarActionPerformed

    private void jpasscontraseniaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jpasscontraseniaKeyTyped
        if(jtxtusuarip.getText().length() > (10 -1)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limite", "Error",JOptionPane.ERROR_MESSAGE);
        }
        char caracteres = evt.getKeyChar();
        if ((caracteres < 48 || caracteres > 57) && (caracteres != 8) && (caracteres != 10)) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Caracter Denegado", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jpasscontraseniaKeyTyped

    private void jpasscontraseniaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jpasscontraseniaKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER) {
            botonEntrar.doClick();
        }
    }//GEN-LAST:event_jpasscontraseniaKeyPressed

    private void jtxtusuaripKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtusuaripKeyTyped
        if(jtxtusuarip.getText().length() > (10 -1)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Limite", "Error",JOptionPane.ERROR_MESSAGE);
        }
        char caracteres = evt.getKeyChar();
        if((caracteres < 65 || caracteres > 90) && (caracteres < 97 || caracteres >122 ) && (caracteres != 8) && (caracteres != 10 ) ){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Caracter Denegado ", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtxtusuaripKeyTyped

